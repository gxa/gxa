  CREATE SEQUENCE  "A2_ORGANISM_SEQ"  
    MINVALUE 1 MAXVALUE 1.00000000000000E+27 
    INCREMENT BY 1 START WITH 1 CACHE 20 
    NOORDER  NOCYCLE;
    

